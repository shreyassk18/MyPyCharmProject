#A function which has no name is called lamda function
#This is also known as anonymous function
#A lambda function can take any number of arguments but can only have one expression

x = lambda a : a + 10   #Here x acts as a function
print(x(5))

y = lambda a,b : a * b
print(y(10,10))

z = lambda a,b,c : a + b + c
print(z(10,20,30))

#This will reduce the number of lines for a function