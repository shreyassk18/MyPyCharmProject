#For loop
for i in range(10):
    print(i, end="\t")
print("\n")

for i in range(5,10):
    print(i, end="\t")
print("\n")

#odd numbers
for i in range(1,10,2):
    print(i, end="\t")
print("\n")

#even numbers
for i in range(0,10,2):
    print(i, end="\t")
print("\n")

#decrement
for i in range(10,1,-1):
    print(i, end="\t")
print("\n")

#negatives
for i in range(-10,-5):
    print(i, end="\t")
print("\n")

#negative increment by 2
for i in range(-10,-5,2):
    print(i, end="\t")
print("\n")